ans=ans1

# 12 * 12 の方眼に解を可視化
for i in range(144):
    if ans[i] != '.':
        y = i // 12
        x = i % 12
        sy = y * 40
        sx = x * 40
        gy = (y + 1) * 40
        gx = (x + 1) * 40
        fi = (238,211,181)
        
        # 色を選択
        if int(ans[i]) < 2:
            fi = (234,235,239)
        elif int(ans[i]) < 4:
            fi = (21,21,21)
        elif int(ans[i]) < 6:
            fi = (149,154,157)
         
        # 首尾を選択
        if int(ans[i]) % 2 == 0:
            draw.ellipse((sx,sy,gx,gy), fill=fi)
        else:
            draw.rectangle((sx,sy,gx,gy), fill=fi)
    
